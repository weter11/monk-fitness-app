    RIGHT_TOES,
    LEFT_KNEE,
    RIGHT_KNEE,
    LEFT_HAND,
    RIGHT_HAND,
    LEFT_ELBOW,
    RIGHT_ELBOW,
    LEFT_FOREARM,
    RIGHT_FOREARM,
    PELVIS,
    BACK,
    HIPS,
    CUSTOM
}
